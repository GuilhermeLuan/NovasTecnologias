num1 = 10
num2 = 20

# Soma
print(num1 + num2)

# Subtração
print(num1 - num2)

# Multiplicação
print(num1 * num2)

# Divisão
print(num1 / num2)

# Divisão inteira
print(num1 // num2)

# Resto da divisão
print(num1 % num2)

# Exponenciação
print(num1 ** num2)

# Atribuição
num1 += 1
print(num1)

num1 -= 1
print(num1)

num1 *= 2
print(num1)

num1 /= 2
print(num1)