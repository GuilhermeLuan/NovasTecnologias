import math

print(round(2.9)) # round(): Arredonda para o inteiro mais próximo
print(math.pi) # math.pi: Retorna o valor de pi
print(abs(-2.9)) # abs(): Retorna o valor absoluto
print(math.pow(2, 3)) # math.pow(): Retorna a potência
print(max(2, 3)) # max(): Retorna o maior valor
print(min(2, 3)) # min(): Retorna o menor valor
print(sum([1, 5, 3, 9])) # sum(): Retorna a soma dos valores
print(ord('A')) # ord(): Retorna o valor ASCII
print(divmod(10, 3)) # divmod(): Retorna a divisão e o resto
print(math.ceil(2.9)) # ceil(): Arredonda para cima
print(math.floor(2.9)) # floor(): Arredonda para baixo