# Funções são blocos de código que só são executados quando são chamados.

def my_function():
    print("Hello from a function")

my_function()

# Funções com parametros

def my_function_with_params(param1, param2):
    print(param1 + param2)

my_function_with_params(1, 2)

# Funções com parametros e retorno

def my_function_with_return(param1, param2):
    return param1 + param2

print(my_function_with_return(1, 2))

